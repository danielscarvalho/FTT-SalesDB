CONNECT SYS

CREATE USER SALESDB
    IDENTIFIED BY SALESDB
    DEFAULT TABLESPACE USERS
    QUOTA 100M ON USERS
    TEMPORARY TABLESPACE TEMP
    QUOTA 100M ON SYSTEM
    PROFILE DEFAULT
    PASSWORD EXPIRE;   

GRANT CREATE SESSION TO SALESDB;
GRANT CREATE TABLE TO SALESDB;

CREATE TABLE COMPANY (
	ID		NUMBER(10) PRIMARY KEY,
	NAME		VARCHAR2(250) NOT NULL,
	URL		VARCHAR2(500),
	ADDRESS		VARCHAR2(250),
	ZIP		VARCHAR2(100),
	MARKET		VARCHAR2(100))
TABLESPACE USERS;

CREATE TABLE OCCUPATION (
	ID		NUMBER(10) PRIMARY KEY,
	OCCUPATION	VARCHAR2(500) NOT NULL,
	CODE		NUMBER(10) NOT NULL UNIQUE)
TABLESPACE USERS;

CREATE TABLE CLIENT    (
	ID 		NUMBER(10) PRIMARY KEY,
	NAME 		VARCHAR2(250) NOT NULL,
	CINDEX 		NUMBER(10,10),
	DOB 		DATE NOT NULL,
	HIRE_DATE	DATE DEFAULT SYSDATE NOT NULL,
	CREDIT 		NUMBER(10,2),
	DAYS 		NUMBER(10),
	ID_COMPANY 	NUMBER(10) REFERENCES COMPANY,
	ID_OCCUPATION 	NUMBER(10) REFERENCES OCCUPATION)
TABLESPACE USERS;

CREATE TABLE PRODUCT (
	ID		NUMBER(10) PRIMARY KEY,
	PART_NUMBER	VARCHAR2(20) NOT NULL UNIQUE,
	DESCRIPTION	VARCHAR2(500) NOT NULL,
	PSIZE		VARCHAR2(10) NOT NULL,
	STATUS		CHAR DEFAULT 'T' NOT NULL,
	VALUE		NUMBER(10,2) NOT NULL)
TABLESPACE USERS;

CREATE TABLE SALE (
	ID 		NUMBER(10) PRIMARY KEY,
	ITEM		NUMBER(10) NOT NULL,
	TRANSACTION	NUMBER(10) NOT NULL,
	ID_CLIENT 	NUMBER(10) NOT NULL REFERENCES CLIENT,
	ID_QUANTITY	NUMBER(10) NOT NULL,
	ID_PRODUCT	NUMBER(10) NOT NULL REFERENCES PRODUCT)
TABLESPACE USERS;	

